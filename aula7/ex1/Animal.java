package edu.br.marielle.aula7.ex1;

public class Animal {

	String nome, cor, ambiente;
	float comprimento, vel_media;
	int n_patas;
	
	public Animal(String nome, String cor, String ambiente, float comprimento, float vel_media, int n_patas) {
		super();
		this.nome = nome;
		this.cor = cor;
		this.ambiente = ambiente;
		this.comprimento = comprimento;
		this.vel_media = vel_media;
		this.n_patas = n_patas;
	}
	
}
